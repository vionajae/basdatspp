    private void muatTabel(){
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        model.getDataVector().removeAllElements();
        String sql = "select * from pembayaran";
        ModelDatabase md = new ModelDatabase();
        ResultSet rs = md.ambilData(sql);
        try {
            while(rs.next()){
                Vector baris = new Vector();
                baris.add(rs.getInt("id_pembayaran"));
                baris.add(rs.getInt("id_petugas"));
                baris.add(rs.getString("nisn"));
                baris.add(rs.getDate("tgl_bayar"));
                baris.add(rs.getString("bulan_dibayar"));
                baris.add(rs.getString("tahun_dibayar"));
                baris.add(rs.getString("id_spp"));               
                baris.add(rs.getString("jumlah_bayar"));
                model.addRow(baris);
            }
        } catch (Exception e) {
            Logger.getLogger(FSpp.class.getName()).log(Level.SEVERE, null, e);
        }
        tabel.setModel(model);
    }
    

