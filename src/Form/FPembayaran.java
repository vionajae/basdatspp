    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        // TODO add your handling code here:
        int no_baris = tabel.getSelectedRow();
        String kode = tabel.getValueAt(no_baris, 0).toString();
        String ip = idp.getText();
        String n = nisn.getText();
        Date t = tgl.getDate();
        String b = bln.getText();
        String h = thn.getText();
        String is = ids.getText();
        String j = jml.getText();
        String sql = "update pembayaran set id_petugas='"+ip+"',nisn='"+n+"',tgl_bayar='"+t+"',bulan_dibayar='"+b+"',tahun_dibayar='"+h+"',id_spp='"+is+"',jumlah_bayar='"+j+"' where id_pembayaran="+kode;
        ModelDatabase md = new ModelDatabase();
        if(md.rubahData(sql)){
            status.setText("Data berhasil diperbaharui");
        }else{
            status.setText("Data gagal diperbaharui");
        }
        kosong();
    }//GEN-LAST:event_updateMouseClicked


