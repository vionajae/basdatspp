    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        // TODO add your handling code here:
        String ip = idp.getText();
        String n = nisn.getText();
        Date t = tgl.getDate();
        String b = bln.getText();
        String h = thn.getText();
        String is = ids.getText();
        String j = jml.getText();
        String sql = "insert into pembayaran(id_petugas,nisn,tgl_bayar,bulan_dibayar,tahun_dibayar,id_spp,jumlah_bayar) values('"+ip+"','"+n+"','"+t+"','"+b+"','"+is+"','"+j+"')";
        ModelDatabase md = new ModelDatabase();
        if(md.rubahData(sql)){
            status.setText("Data berhasil ditambahkan");
            muatTabel();
        }else{
            status.setText("Data gagal ditambahkan");
        }
        kosong();
    }//GEN-LAST:event_simpanMouseClicked

